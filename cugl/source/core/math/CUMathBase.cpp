#include <cugl/core/math/CUMathBase.h>

/**
 * Returns the power of two greater than or equal to x
 *
 * @param x		The original integer
 *
 * @return the power of two greater than or equal to x
 */
Uint32 nextPOT(Uint32 x) {
    x = x - 1;
    x = x | (x >> 1);
    x = x | (x >> 2);
    x = x | (x >> 4);
    x = x | (x >> 8);
    x = x | (x >>16);
    return x + 1;
}
